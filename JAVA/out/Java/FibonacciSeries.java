import java.util.Scanner;

public class FibonacciSeries {

    public static void main(String[] args) {

        int i = 23;
        System.out.println(i);
        System.out.println(i++);

        int b = i * 2;
        System.out.println(i + " x 2 = " + b);

        System.out.println(++i);
        System.out.println(i);

        System.out.println(i + " x 2 = " + b);
    }
}