public class EscapeSequence {

    public static void main(String[] args) {

        // \t = Instert the TAB in the text at this point
        System.out.println("Hello\tHow ");

        // * \b = Inserts a BACKSPACE at this point */
        System.out.println("Hello \bWelcome");

        // * \n = Inserts a NEW LINE at this point */
        System.out.println("Hello \nHow Are You");

        // * \r = Insert a CARRIAGE RETURN in the text at this point */
        System.out.println("Hello \rMy Name Is Niyamat");

        // * \f = Inserts a FOMFEED(NEW LINE) at this point in text */
        System.out.println("Hello \fMy Name is Niyamat");

        // * \' = Inserts the SINGLE QUOTE*/
        System.out.println("I said \'OO laa laa\' to her");

        // * \" = INserts DOUBLE QUOTE */
        System.out.println("I said \"Hello !\" to her");

        // * \\ = Inserts a BACKSLASH(\) character at this point in text */
        System.out.println("I am \\ Niyamat");

    }
}
